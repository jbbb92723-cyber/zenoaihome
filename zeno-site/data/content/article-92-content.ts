export const article92Content = `一人公司最稀缺的资源不是钱，是注意力。

你只有一个人。你的注意力被分散到十件事上，每件事都做不好。但如果你的全部注意力集中在一件事上，你能把这件事做到别人望尘莫及。

## 每天只有一件事是最重要的

我每天坐下来，第一件事不是打开微信，不是刷文章，不是看邮件。是问自己：今天如果只能完成一件事，哪件事对我最有价值？

答案通常是：写一篇文章、帮一个客户做报价判断、改进我的工作系统。

然后我在上午做这件事。上午是我的深度工作时间——手机静音、微信关掉、不受打扰的两小时。

下午再做那些不需要深度注意力的事：回信息、处理邮件、整理记录。这些事需要做，但不值钱。不能让它们吃掉值钱的时间。

## 一周只排三件大事

不要写七天的待办清单。你会发现自己第一天就写了八件事，一周结束只做了三件。

我每周一只列三件最重要的事。不是"这周要做的事"，是"这周非做不可的事"。这三件事做完了，这周就成功了。其他的都是加分项。

三件事定好了就不改了。如果这周突然有个"紧急"的事冒出来——基本都不是真的紧急，是别人觉得紧急。让它在下周的清单里排队。

## 拒绝是好能力

你是一个人。你接了A客户，就没时间服务B客户。你同意做一个免费咨询，就没时间做付费项目。

一人公司要学会的话："这个我帮不了你，但我可以推荐一个人。"或者"现在排满了，下个月可以联系我。"

不是傲慢，是诚实。你能做好的事是有限的，承认这一点反而让客户更尊重你。`