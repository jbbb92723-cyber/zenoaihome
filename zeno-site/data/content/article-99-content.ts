export const article99Content = `一人公司最容易犯的错不是懒惰——是太勤快。

看到一个机会就想抓。客户问能不能多做一个服务，你说可以。朋友拉你做一个新项目，你说试试。结果时间碎了一地。没一个方向是深的。

## 你永远只能做好一件事

道理很简单，执行太难。

我能做好报价判断，也能做床垫选购。但如果再加全案设计、再加施工监理、再加风水——结果每个都做得很普通。

一人公司的竞争力不是你会多少东西。是你是不是把一件事做到了别人做不到的程度。

到那个程度需要你把其他的全砍了。不是永远不做——是现在不做。

## 我砍掉的东西

施工监理。有人问我有没有空去工地盯进度。我说有，但不接了。去了工地就没时间写文章、做判断。后面两件对我的长期价值更大。

风水。不是没用——是跟现在要做的事不搭。掺在一起客户搞不清楚你的正业。

什么都要帮的免费咨询。现在有人微信上来问问题，能一句话回的我就回。要深入看的我会说：这是我的付费服务，价格是X。需要的话发材料给我。

## 怎么判断一个东西该不该砍

问自己：这件事不做，我的核心业务会不会受影响？不会——砍。会但不是核心——先停，等核心稳了再说。

一个人最稀缺的东西不是才华，是注意力。把它全押在最值钱的事上。`