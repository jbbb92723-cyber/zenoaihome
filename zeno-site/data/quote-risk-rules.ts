export type QuoteRiskRule = {
  id: string
  slug: string
  name: string
  category: string
  riskLevel: '低' | '中' | '高'
  oneLine: string
  plainExplanation: string
  whyItMatters: string
  triggerTerms: string[]
  commonItems: string[]
  homeownerMisjudgment: string
  preSigningQuestions: string[]
  contractSuggestion: string
  suitableFor: string[]
  relatedRiskIds: string[]
  /** legacy compatibility for existing pages */
  title?: string
  trigger?: string
  why?: string
  ask?: string
}

export interface QuoteRiskDimension {
  id: string
  key:
    | 'hasItemizedQuote'
    | 'hasMaterialBrand'
    | 'hasProcessDescription'
    | 'hasMeasurementRule'
    | 'hasChangeOrderRule'
    | 'hasAcceptanceStandard'
    | 'hasWarrantyScope'
    | 'hasPaymentMilestone'
  dimension: string
  label: string
  risk: string
  question: string
  weight: number
}

export interface QuoteRiskDictionaryItem {
  id: string
  term: string
  plain: string
  whatToLookFor: string
}

function defineRule(rule: Omit<QuoteRiskRule, 'title' | 'trigger' | 'why' | 'ask'>): QuoteRiskRule {
  return {
    ...rule,
    title: rule.name,
    trigger: rule.triggerTerms.slice(0, 4).join(' / '),
    why: rule.whyItMatters,
    ask: rule.preSigningQuestions[0] ?? '',
  }
}

