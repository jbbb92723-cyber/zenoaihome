报价单里有些项目不含，其实不一定有问题。

只要提前写清楚，业主知道自己没买什么，就可以做选择。

真正麻烦的是前后冲突。

一处写含。

一处写不含。

这会让双方都觉得自己有理由。

业主会说：

> 明细里明明写了垃圾外运，为什么还要加钱？

施工方可能说：

