- 大玻璃是否需要吊装
- 安装后怎么打胶
- 外侧防水怎么处理
- 内侧收口谁负责
- 是否符合物业外立面要求

这些都不是小事。

如果报价只写材料，不写安装边界，后面很容易加钱或扯皮。

