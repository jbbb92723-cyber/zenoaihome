def chapter_filename(book: dict, ch: dict) -> str:
    file_slug = FILE_SLUGS.get(book["slug"], {}).get(ch["label"], slugify(ch["title"]))
    return f"{ch['label']}-{file_slug}.md"


def bullets(items: list[str]) -> str:
    return "\n".join(f"- {item}" for item in items)


def numbered(items: list[str]) -> str:
    return "\n".join(f"{idx}. {item}" for idx, item in enumerate(items, 1))


def chapter_markdown(ch: dict) -> str:
    methods = "\n".join(
        f"- **{m['name']}**: {m['use']}\n  - 怎么做: {m['how']}"
        for m in ch.get("methods", [])
    )
    concepts = "\n".join(f"- **{k}**: {v}" for k, v in ch.get("concepts", {}).items())
    anti = bullets(ch.get("anti", []))
    takeaways = numbered(ch.get("takeaways", []))
    example = ch.get("example", "")
    connects = bullets(ch.get("connects", []))
    return f"""
    # {ch['label']}: {ch['title']}

    ## 核心用途

    {ch['core']}

    ## 可调用方法

    {methods}

    ## 关键术语

    {concepts}

    ## 误用提醒

    {anti}

    ## Worked Example

    {example}

    ## 行动指南

    {takeaways}

    ## Connects To

    {connects}
    """


