// ─── 查询函数 ────────────────────────────────────────────

export function getTemplatesByCategory(uiCategory: string): Template[] {
  if (uiCategory === 'all') return ALL_TEMPLATES
  return ALL_TEMPLATES.filter((t) => t.category === uiCategory)
}

export function getTemplatesBySystemCategory(systemCategory: string): Template[] {
  return ALL_TEMPLATES.filter((t) => t.systemCategory === systemCategory)
}

export function getTemplateById(id: string): Template | undefined {
  return ALL_TEMPLATES.find((t) => t.id === id)
}
