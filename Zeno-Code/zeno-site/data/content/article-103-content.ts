export const article103Content = `我帮客户看报价这件事，做了十六年了。以前是一件一件做——客户发来，我看完回复。这是手工活。

去年我开始把这套东西往产品方向改。原因很简单：我一个人，每天能看的报价是有限的。我睡着的时候有人想找我帮他看，找不到我。第二天醒来，消息过期了。

## 先做了个清单

我把每次看报价都会检查的东西写了下来。哪些项目容易漏，哪些描述太模糊，哪些条款要改。写了一页纸。

这页纸就是我的第一个产品。它是免费的——别人拿这张清单自己也能初步看一遍报价。看完发现有东西看不懂，来问我。这时候再付费让我深度看，对他对我都更高效。

## 然后做了个工具

清单得自己一条条对照，还是累。我就把这个清单做成了一个在线工具——你填几个关键信息，工具自动给你标风险。

这个工具现在在我的网站上。免费的。它不会替代我——它帮你做的事情只是标记，不是判断。判断还是要我来。但它帮我省掉了一个环节：我不需要再逐条告诉客户你这里可能有问题。工具已经标了，我就直接讲为什么有问题、怎么处理。

## 接下来

把你的服务里反复做的那部分拿出来。写成一个清单、做一个模板、录一段视频——不管什么形式。只要能让人在你不在的时候也能用。

你不在的时候你的东西在帮人。等你回来，他自然来找你。`