不需要辞职。不需要注册公司。不需要先投一笔钱。

就在你现在的工作之外，选一个你判断力最强的领域，开始做一件事：

写一篇帮人解决问题的文章。录一段讲清楚某个常见误区的视频。接一个愿意付费的咨询，看看你的判断到底值多少钱。

做了之后你才知道市场认不认。市场不认，调整方向。市场认了，注册公司的事后面再说。

