export const article98Content = `我在网上看到太多"一人公司年入百万"的内容。每次看到我都在想：这些人在卖课还是在做生意。

我做了一人公司两年。今天把我真实的收入结构写出来。不是为了证明什么——是让你有一个真实的参照。

## 第一年：几乎没有收入

第一年我的收入几乎为零。线下装修咨询偶尔接几个，一个收几千。线上全是免费的——免费写文章、免费回答评论里和微信上的问题。

我不是不想收费。是我还不确定我的服务值多少钱，也不知道客户愿不愿意付。

这一年我在做的事：写了几十篇文章，测试了几个免费工具，搞清楚了我的核心判断能力到底能帮人解决什么问题。这些东西在账面上不产生收入，但它们是第二年收入的基础。

## 第二年：开始稳定

免费工具继续做。开始有客户从文章里找到我，加微信来问服务。我把服务分成几个层级，标了价格。

报价初查199块。快审499。深度综合判断1999。每个月大概接10-20个客户。月收入在5000-15000之间浮动——取决于当月接了多少深度服务。

这收入不高。比很多上班族低。但它是从零开始的——是纯利润，没有房租、没有员工工资、不用给平台分成。

## 你不需要一开始就全职做

我的建议：先兼职做一年。用主业养副业。当你的副业收入超过主业的一半，并且持续三个月以上，再考虑全职。

一人公司最大的风险不是"赚不到钱"——是"你还没建立稳定的客户来源就辞职了"。客户来源需要时间，你急不来。`