export const article90Content = `我做一人公司两年，最难的不是写文章，不是做判断——是定价。

刚开始的时候，有人加微信来问报价审核。我不好意思收钱，说"你自己看着给吧"。对方发了个红包——88块。我花了半小时看报价、半小时写回复、半小时跟他解释。相当于每小时不到60块。比我在工地盯一天还低。

后来我发现，定价的核心不是"你付出了多少时间"，是"客户从你这里拿走了什么价值"。

## 客户付你钱，不是因为你需要时间，是因为他需要答案

一个客户来找你做报价审核。他的报价单总价20万。你帮他看出了5个风险点，其中2个可能让他多花2万。你收他499。

他是花499买了一单审核服务，还是用499避免了20000的潜在损失？从客户的角度看，你收499是帮他省了2万。从你的角度看，你觉得自己花了1小时，每小时不到500块。觉得够不够？

如果你按时间算，你可能觉得499一小时可以了。如果你按价值算，你可能觉得499太便宜了——你帮他省了2万，他付你1999都划算。

## 一人公司定价三步法

**第一步：搞清楚你的服务帮客户避免了多大的损失。** 你帮人看报价，避免的是3-5年内持续出现的质量问题和额外花费。这个损失有多大？少则几千，多则几万。

**第二步：定一个你自己不会心疼的价格。** 不是市场价，不是你朋友的价，是你做完这件事之后不觉得亏的价格。这个价格很难用理性解释，但你心里知道。低于这个价你下次不想接这个客户的活。

**第三步：写出来，公开标价。** 不要藏在微信里。标在网站上了，客户来找你之前就知道要花多少钱。你能过滤掉只想"问一下"但不想付费的人，也让你自己心里踏实——你报价了，不用再当面不好意思说价格。

## 价格不是越低越好

我做免费工具的时候有一个发现：免费工具的使用率比付费服务高十几倍，但真正有价值的反馈和长期客户——全是付费服务来的。

免费吸引来的更多是"看看"的人。付费留下的是"真的需要"的人。

你定一个自己觉得对得起的价格。有人嫌贵，那他不适合你的服务，没关系。有人付了钱，你做完服务他真心感谢你——这种客户你会想服务他一辈子。`