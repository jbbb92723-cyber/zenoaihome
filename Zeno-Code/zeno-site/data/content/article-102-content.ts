export const article102Content = `AI帮你写的草稿，你怎么把它改成你看起来是你写的东西？这个问题我自己搞了大半年。

一开始我是让AI改写。改完还是AI味。再改。再改。改了三遍，文章越来越奇怪——像两个人抢着写同一段话。

后来我放弃了让AI帮我改的想法。改成一个笨办法：AI只整理信息，我自己写。写完之后让AI帮我做三件事——检查漏没漏、标出连我都没注意到的矛盾、把太啰嗦的句子指出来。

其余的我自己改。具体的改法：

**删因此然而此外这些词。** 这类词删掉之后如果读起来还顺，说明原文逻辑是自洽的。如果删掉之后断了，不是该把连接词加回去——是前面的内容少了一句你没说的话。

**长短交错。** AI写的句子都是匀速的——每句25到35个字。读十句你就不想读了。改的时候在这十句中间插两句短的。三个字：不对。 四个字：举个例子。 打破那个节奏。

**把你嘴上会说的词换进去。** 近日换成前两天。倘若换成如果。大致换成差不多。这些词不需要思考——你就是知道换完之后舒服了。

**读完出一身冷汗的部分不要改。** 有时候你写完一段话觉得这段是不是太直接了会不会有人看了不舒服。这种地方留着。你犹豫该不该删的，八成是真的。

最好的状态是：你把改好的文章发给一个认识你的人，让他读一遍。如果他读完之后说像你写的——成了。`